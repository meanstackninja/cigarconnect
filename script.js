/* Create a cigar object constructor*/

function Cigar(brand,type,price){
    this.brand = brand;
    this.type = type;
    this.price = price;
}

function Location(streetNum,streetName,city,state,zip,phoneNumber,siteUrl,bLounge,bInetAccess){
    this.streetNum = streetNum;
    this.streetName = streetName;
    this.city = city;
    this.state = state;
    this.zip = zip;
    this.phoneNumber = phoneNumber;
    this.sitUrl = siteUrl;
    this.bLounge = bLounge;
    this.bInetAccess = bInetAccess;


}


